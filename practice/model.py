import numpy as np


def tic_ode_system(
    t,
    y,
    a,
    b,
    c,
    mu,
    d,
    p,
    lmbda,
    eta_c_func,
    eta_mu_func,
    s_A_func,
    s_C_func,
) -> np.ndarray:
    """
    Определяет правую часть системы обыкновенных дифференциальных уравнений T-I-C.

    :param t: Текущее время.
    :param y: Вектор состояния [T, I, C], где:
              T - концентрация опухолевых клеток
              I - концентрация иммунных эффекторных клеток
              C - концентрация цитокинов
    :param a: Темп роста опухоли.
    :param b: Коэффициент насыщения для роста опухоли.
    :param c: Коэффициент взаимодействия между опухолью и иммунными клетками.
    :param mu: Естественная смертность иммунных клеток.
    :param d: Коэффициент стимуляции иммунных клеток опухолью.
    :param p: Коэффициент стимуляции иммунных клеток цитокинами.
    :param lmbda: Коэффициент распада цитокинов.
    :param eta_c_func: Функция терапии, влияющая на взаимодействие опухоли и иммунных клеток.
    :param eta_mu_func: Функция терапии, влияющая на смертность иммунных клеток.
    :param s_A_func: Функция терапии, стимулирующая иммунные клетки.
    :param s_C_func: Функция терапии, стимулирующая производство цитокинов.

    :return: array_like: Вектор производных [dT/dt, dI/dt, dC/dt].
    """
    T, I, C = y

    # Защита от отрицательных концентраций, которые не имеют биологического смысла
    # Применяем max(0, x) к концентрациям, используемым в расчетах производных
    # Это предотвращает математические проблемы и отражает биологическую реальность
    T = max(0.0, T)
    I = max(0.0, I)
    C = max(0.0, C)

    eta_c = eta_c_func(t)
    eta_mu = eta_mu_func(t)
    s_A = s_A_func(t)
    s_C = s_C_func(t)

    dTdt = a * T * (1 - b * T) - (c + eta_c) * T * I
    dIdt = (d + p * C) * T * I - (mu - eta_mu) * I + s_A
    dCdt = s_C - lmbda * C

    return np.array([dTdt, dIdt, dCdt])
